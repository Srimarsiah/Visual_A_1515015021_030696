    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
       Object[] data = new Object[3];
       data[0] = nama.getText();
       data[1] = no.getText();
       data[2] = asal.getSelectedItem().toString();
        
       model.addRow(data);
       
    }//GEN-LAST:event_tambahActionPerformed

    private void tambahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tambahKeyReleased
        // TODO add your handling code here:
         if(nama.getText().equals("") || no.getText().equals("")){
            tambah.setEnabled(false);
        }else {
            tambah.setEnabled(true);
        }
    }//GEN-LAST:event_tambahKeyReleased

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
       Object[] data = new Object[3];
       data[0] = nama.getText();
       data[1] = no.getText();
       data[2] = asal.getSelectedItem().toString();
       
       model.addRow(data);
        JOptionPane.showMessageDialog(null, "Data Tersimpan");
    }//GEN-LAST:event_simpanActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        int baris = jTable2.getSelectedRow();
        jTable2.setValueAt(nama.getText(), baris,0);
        jTable2.setValueAt(no.getText(), baris,1);
        jTable2.setValueAt(asal.getSelectedItem(), baris,2);
    }//GEN-LAST:event_updateActionPerformed

