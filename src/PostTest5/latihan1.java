    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
         int baris = jTable2.getSelectedRow();
        String nama_edit = jTable2.getValueAt(baris, 0).toString();
        String notelepon_edit = jTable2.getValueAt(baris, 1).toString();
        String kotaasal_edit = jTable2.getValueAt(baris, 2).toString();
        
        nama.setText(nama_edit);
        no.setText(notelepon_edit);
        asal.setSelectedItem(kotaasal_edit);
    }//GEN-LAST:event_jTable2MouseClicked

